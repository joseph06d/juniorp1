    public void mtd_limpiartextos() {
        txt_modelo.setText("");
        txt_placa.setText("");
        txt_marca.setText("");
        txt_año.setText("");
        txt_precio.setText("");
    }

    private Boolean validacion() {
        if (txt_modelo.getText().length() <= 0) {
            JOptionPane.showMessageDialog(null, "Por favor ingrese el modelo", "Sistema de Vehiculos",
                    JOptionPane.INFORMATION_MESSAGE);
            return false;
        }
        if (txt_placa.getText().length() <= 0) {
            JOptionPane.showMessageDialog(null, "Por favor ingrese la placa", "Sistema de Vehiculos",
                    JOptionPane.INFORMATION_MESSAGE);
            return false;
        }
        if (txt_marca.getText().length() <= 0) {
            JOptionPane.showMessageDialog(null, "Por favor ingrese la marca", "Sistema de Vehiculos",
                    JOptionPane.INFORMATION_MESSAGE);
            return false;
        }
        if (txt_año.getText().length() <= 0) {
            JOptionPane.showMessageDialog(null, "Por favor ingrese el año", "Sistema de Vehiculos",
                    JOptionPane.INFORMATION_MESSAGE);
            return false;
        }
        if (txt_precio.getText().length() <= 0) {
            JOptionPane.showMessageDialog(null, "Por favor ingrese el precio", "Sistema de Vehiculos",
                    JOptionPane.INFORMATION_MESSAGE);
            return false;
        }

        return true;
    }

    private Boolean validarRepeticionCedula() {
        String codigoNuevo = txt_marca.getText();

        for (int i = 0; i < tbl_carros.getRowCount(); i++) {
            if (tbl_carros.getValueAt(i, 1).toString().equalsIgnoreCase(codigoNuevo)) {
                return true;
            }
        }

        return false;
    }
    
    private Boolean validarRepeticionSeguro() {
        String codigoNuevo = txt_año.getText();

        for (int i = 0; i < tbl_carros.getRowCount(); i++) {
            if (tbl_carros.getValueAt(i, 3).toString().equalsIgnoreCase(codigoNuevo)) {
                return true;
            }
        }

        return false;
    }

