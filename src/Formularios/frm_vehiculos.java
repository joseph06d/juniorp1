    private void btn_agregarActionPerformed(java.awt.event.ActionEvent evt) {
        String accion = btn_agregar.getText();
        try {
            if (accion.equalsIgnoreCase("Agregar")) {
                if (validarRepeticionCedula()) {
                    JOptionPane.showMessageDialog(null, "Error: La marca ya existe. Ingrese una diferente.");
                    return;
                }
                
                if (validarRepeticionSeguro()) {
                    JOptionPane.showMessageDialog(null, "Error: El año ya existe. Ingrese una diferente.");
                    return;
                }

                
                if (validacion()) {
                    Clases.cls_vehi objeto = new Clases.cls_vehi(
                            txt_placa.getText(),
                            txt_marca.getText(),
                            txt_modelo.getText(),
                            Integer.parseInt(txt_año.getText()),
                            Double.parseDouble(txt_precio.getText()));

                    modelo = (DefaultTableModel) tbl_carros.getModel();

                      String nuevaFila[] = {
                        objeto.getPlaca(),
                        objeto.getMarca(),
                        objeto.getModelo(),
                        String.valueOf(objeto.getAño()),
                        String.valueOf(objeto.getPrecio()),
                    };

                    modelo.addRow(nuevaFila);
                    mtd_limpiartextos();
                }
            } else if (accion.equalsIgnoreCase("Actualizar")) {
                int filasel = tbl_carros.getSelectedRow();
                if (filasel == -1) {
                    JOptionPane.showMessageDialog(null, "Por favor seleccione una fila");
                    return;
                }

                if (validacion()) {
                    Clases.cls_vehi objeto = new Clases.cls_vehi(
        txt_placa.getText(),
        txt_marca.getText(),
        txt_modelo.getText(),
        Integer.parseInt(txt_año.getText()),
        Double.parseDouble(txt_precio.getText()));

                    tbl_carros.setValueAt(txt_placa.getText(), filasel, 0);
                    tbl_carros.setValueAt(txt_marca.getText(), filasel, 1);
                    tbl_carros.setValueAt(txt_modelo.getText(), filasel, 2);
                    tbl_carros.setValueAt(txt_año.getText(), filasel, 3);
                    tbl_carros.setValueAt(txt_precio.getText(), filasel, 4);

                    mtd_limpiartextos();
                    btn_agregar.setText("Agregar");
                }
            }
        } catch (HeadlessException | NumberFormatException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }

    private void btn_eliminarActionPerformed(java.awt.event.ActionEvent evt) {
        try {
            int filasel = tbl_carros.getSelectedRow();
            if (filasel == -1) {
                JOptionPane.showMessageDialog(null, "Por favor selecione una fila");
            } else {
                modelo = (DefaultTableModel) tbl_carros.getModel();
                modelo.removeRow(filasel);
            }
        } catch (HeadlessException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }

    private void btn_editarActionPerformed(java.awt.event.ActionEvent evt) {
        try {
            int filasel = tbl_carros.getSelectedRow();

            if (filasel == -1) {
                JOptionPane.showMessageDialog(null, "Por favor seleccione una fila");
                return;
            }

            
            txt_placa.setText(tbl_carros.getValueAt(filasel, 0).toString());
            txt_marca.setText(tbl_carros.getValueAt(filasel, 1).toString());
            txt_modelo.setText(tbl_carros.getValueAt(filasel, 2).toString());
            txt_año.setText(tbl_carros.getValueAt(filasel, 3).toString());
            txt_precio.setText(tbl_carros.getValueAt(filasel, 4).toString());

            btn_agregar.setText("Actualizar");

        } catch (HeadlessException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }

